numero_entero = 5
numero_negativo = -10
numero_flotante = 3.14
numero_negativo_flotante = -0.5
nombre = "Juan"
mensaje = "Hola, mundo!"
verdadero = True
falso = False
valor_nulo = None

# Mostrar el tipo de dato de cada variable
print(type(numero_entero))  # <class 'int'>
print(type(numero_negativo))  # <class 'int'>
print(type(numero_flotante))  # <class 'float'>
print(type(numero_negativo_flotante))  # <class 'float'>
print(type(nombre))  # <class 'str'>
print(type(mensaje))  # <class 'str'>
print(type(verdadero))  # <class 'bool'>
print(type(falso))  # <class 'bool'>
print(type(valor_nulo))  # <class 'NoneType'>